#!/usr/bin/env node
/**
 * Generate src/lib/supabase-rows.ts from schemas/*.upsert.schema.json.
 * Run via: npm run generate:rows
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

function loadSchema(name) {
  return JSON.parse(
    readFileSync(join(root, "schemas", name), "utf8")
  );
}

function schemaType(def) {
  if (def.enum) {
    return def.enum.map((v) => JSON.stringify(v)).join(" | ");
  }
  if (Array.isArray(def.type)) {
    const parts = def.type
      .filter((t) => t !== "null")
      .map((t) => (t === "integer" ? "number" : t));
    const base = parts.length === 1 ? parts[0] : parts.join(" | ");
    return def.type.includes("null") ? `${base} | null` : base;
  }
  if (def.type === "array") {
    const item = schemaType(def.items);
    return `${item}[]`;
  }
  if (def.type === "integer") return "number";
  return def.type ?? "unknown";
}

function emitInterface(name, schema) {
  const required = new Set(schema.required ?? []);
  const lines = [
    `/** Generated from ${schema.$id ?? "schema"} — do not edit by hand. */`,
    `export interface ${name} {`,
  ];
  for (const [key, def] of Object.entries(schema.properties ?? {})) {
    const optional = required.has(key) ? "" : "?";
    lines.push(`  ${key}${optional}: ${schemaType(def)};`);
  }
  lines.push("}");
  return lines.join("\n");
}

function emitConstEnum(constName, typeName, values) {
  return [
    `export const ${constName} = [`,
    ...values.map((v) => `  ${JSON.stringify(v)},`),
    `] as const;`,
    `export type ${typeName} = (typeof ${constName})[number];`,
  ].join("\n");
}

const events = loadSchema("events.upsert.schema.json");
const stories = loadSchema("stories.upsert.schema.json");

const out = `// Code generated by scripts/generate-row-types.mjs. DO NOT EDIT.

${emitConstEnum("EVENT_CATEGORIES", "EventCategory", events.properties.category.enum)}
${emitConstEnum("EVENT_SOURCES", "EventSource", events.properties.source.enum)}

${emitInterface("EventRow", events)}

${emitInterface("StoryRow", stories)}
`;

writeFileSync(join(root, "src/lib/supabase-rows.ts"), out);
console.log("wrote src/lib/supabase-rows.ts");
